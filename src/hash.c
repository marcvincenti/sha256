/*
 * Calculate w_t with a given 512-bit chunk
 */
value** process_wt(cnf* cnf, value** w_t, value** chunk) {
  int i;
  value** s0, ** s1, ** r0, ** r1, ** res;
  memcpy(w_t, chunk, sizeof(value*) * 512);
  for (i = 16; i < 64; i++ ) {
    s0 = bool_s_0(cnf, &w_t[(i-15)*32]);
    r0 = bool_add_32(cnf, s0, &w_t[(i-16)*32]);
    s1 = bool_s_1(cnf, &w_t[(i-2)*32]);
    r1 = bool_add_32(cnf, s1, &w_t[(i-7)*32]);
    res = bool_add_32(cnf, r0, r1);
    memcpy(&w_t[i*32], res, sizeof(value*) * 32);
    free(s0);
    free(s1);
    free(r0);
    free(r1);
    free(res);
  }
  return w_t;
}

/*
 * Make one round of SHA-2 function
 */
value** iterate(cnf*cnf, value** h_t, value** w, value** const_sha) {
  value** t1 = bool_add_32(cnf, w, const_sha);
  value** t2 = bool_add_32(cnf, t1, &h_t[224]);
  value** e1 = bool_e_1(cnf, &h_t[128]);
  value** t3 = bool_add_32(cnf, t2, e1);
  value** ch = bool_ch(cnf, &h_t[128], &h_t[160], &h_t[192]);
  value** T1 = bool_add_32(cnf, t3, ch);
  value** t4 = bool_add_32(cnf, T1, &h_t[96]);
  value** e0 = bool_e_0(cnf, h_t);
  value** maj= bool_maj(cnf, h_t, &h_t[32], &h_t[64]);
  value** T2 = bool_add_32(cnf, e0, maj);
  value** t5 = bool_add_32(cnf, T1, T2);
  memcpy(&h_t[160], &h_t[128], sizeof(value*) * 96);
  memcpy(&h_t[128], t4, sizeof(value*) * 32);
  memcpy(&h_t[32], h_t, sizeof(value*) * 96);
  memcpy(h_t, t5, sizeof(value*) * 32);
  free(t1); free(t2); free(t3); free(t4); free(t5);
  free(e0); free(e1);
  free(ch); free(maj);
  free(T1); free(T2);
  return h_t;
}

/*
 * Update current hash value given h_t and w_t
 */
value** update(cnf* cnf, value** h_t, value** w_t) {
  int i, j;
  value** initial_h_t = memcpy(malloc(sizeof(value*) * 256), h_t, sizeof(value*) * 256);
  value** const_values = malloc(sizeof(value*) * 64 * 32);
  value** temp;
  /* Initialize const values */
  for (i = 0; i < 64; i++) {
    for (j = 0; j < 32; j++) {
      const_values[i*32+j] = new_boolean(CONST_SHA_VALUES[i][j]);
    }
  }
  /* Compression function main loop */
  for (i = 0; i < 64; i++) {
    h_t = iterate(cnf, h_t, &w_t[i*32], &const_values[i*32]);
  }
  free(const_values);
  /* Add the compressed chunk to the current hash value */
  for (i = 0; i < 8; i++) {
    temp = bool_add_32(cnf, &initial_h_t[i*32], &h_t[i*32]);
    memcpy(&h_t[i*32], temp, sizeof(value*) * 32);
    free(temp);
  }
  free(initial_h_t);
  return h_t;
}

/*
 * Process an input of bytes and return a list of bits
 */
value** hash(cnf* cnf, char* input, int nonce_size) {
  value** h_t = malloc(sizeof(value*) * 256);
  value** w_t = malloc(sizeof(value*) * 2048);
  value** chunks = preProcessInput(cnf, input, nonce_size);
  int nb_blocks = nbBlocksNeeded(strlen(input) << 3, nonce_size);
  int i;
  /* initialise h_t with INIT_SHA_VALUES */
  for (i = 0; i < 256; i++) {
    h_t[i] = new_boolean(INIT_SHA_VALUES[i]);
  }
  /* then update h_t with each blocks */
  for (i = 0; i < nb_blocks; i++) {
    /* prepare w_t */
    process_wt(cnf, w_t, &chunks[512*i]);
    /* update h_t with w_t */
    h_t = update(cnf, h_t, w_t);
  }
  free(w_t);
  free(chunks);
  return h_t;
}
